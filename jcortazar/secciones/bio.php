    Julio Florencio Cortázar nace en Bruselas en 1914. El propio Cortázar relata los primeros años de su vida en una
    carta enviada desde París en 1963: “Nací en Bruselas en agosto de 1914. Signo astrológico, Virgo; por consiguiente,
    asténico, tendencias intelectuales, mi planeta es Mercurio y mi color el gris (aunque en realidad me gusta el
    verde). Mi nacimiento fue un producto del turismo y la diplomacia; a mi padre lo incorporaron a una misión comercial
    cerca de la legación argentina en Bélgica, y como acababa de casarse se llevo a mi madre a Bruselas. Me tocó, nacer
    en los días de la ocupación de Bruselas por los alemanes, a comienzos de la primera guerra mundial. Tenía casi
    cuatro años cuando mi familia pudo volver a la Argentina; hablaba sobre todo francés, y de el me quedo la manera de
    pronunciar la «r», que nunca pude quitarme. Crecí en Banfield, pueblo suburbano de Buenos Aires, en una casa con un
    gran jardín lleno de gatos, perros, tortugas y cotorras: el paraíso. Pero en ese paraíso yo era Adán, en el sentido
    de que no guardo un recuerdo feliz de mi infancia; demasiadas servidumbres, una sensibilidad excesiva, una tristeza
    frecuente, asma, brazos rotos, primeros amores desesperados.”

    Cursa estudios en la Escuela Normal de Profesores Mariano Acosta (cuya atmósfera recreará en el cuento La escuela de
    noche).En 1932 obtiene el título de Maestro Normal, que lo habilita para ejercer el magisterio. Ese mismo año
    intenta sin éxito viajar a Europa en un buque de carga, con un grupo de amigos (fracaso que podemos encontrar
    explicitado en Lugar llamado Kindberg). Ese mismo año, en una librería de Buenos Aires, descubre el libro Opio, de
    Jean Cocteau, cuya lectura cambia "por completo" su visión de la literatura y le ayuda a descubrir el surrealismo.
    En 1935 obtiene el título de Profesor Normal en Letras e ingresa en la Facultad de Filosofía y letras. Aprueba el
    primer año, pero como en su casa "había muy poco dinero y yo quería ayudar a mi madre", abandona los estudios para
    iniciarse en el profesorado. Dos años después es designado profesor en el Colegio Nacional de una pequeña ciudad de
    la provincia de Buenos Aires, Bolívar. Lee infatigablemente y escribe cuentos que no publica.

    En 1938 publica su primera colección de poemas, Presencia con el seudónimo de Julio Denis. En julio de 1939 fue
    trasladado a la Escuela Normal de Chivilcoy.

    En 1941, con el seudónimo Julio Denis, publica un artículo sobre Rimbaud en la revista Huella, que junto con la
    revista Canto fueron importantes vehículos de expresión para los jóvenes escritores. Se traslada a Cuyo, Mendoza, y
    en su Universidad imparte cursos de Literatura Francesa. Publica su primer cuento, Bruja, en la revista Correo
    Literario. Participa en manifestaciones de oposición al peronismo. Cuando Juan Domingo Perón gana las elecciones
    presidenciales presenta su renuncia.

    Reúne un primer volumen de cuentos, La otra orilla. Regresa a Buenos Aires, donde comienza a trabajar en la Cámara
    Argentina del Libro.

    En 1946 publica el cuento Casa tomada en la revista Los ananes de Buenos Aires, dirigida por Jorge Luis Borges. Ese
    mismo año publica un trabajo sobre el poeta inglés John Keats, La urna griega en la poesía de John Keats en la
    Revista de Estudios Clásicos de la Universidad de Cuyo. Colabora en varias revistas, Realidad, entre otras. Publica
    un importante trabajo teórico, Teoría del Túnel.

    En 1948 obtiene el título de traductor público de inglés y francés, tras cursar en apenas nueve meses estudios que
    normalmente insumen tres años. El esfuerzo le provoca síntomas neuróticos, uno de los cuales (la búsqueda de
    cucarachas en la comida) desaparece con la escritura de un cuento, Circe, que junto con Casa Tomada y Bestiario
    (aparecidos en Los anales de Buenos Aires) será incluído más adelante en Bestiario.

    Publica su primer libro de cuentos Bestiario. Obtiene una beca del gobierno francés y viaja a París, con la firme
    intención de establecerse allí. Comienza a trabajar como escritor en la UNESCO.

    En 1953 se casa con Aurora Bernárdez.

    En 1959 publica Las armas secretas (Ed, Sudamericana), que incluye el cuento largo El perseguidor. Este cuento
    supone un sesgo en la narrativa de Cortázar. Allí aborda "un problema de tipo existencial, de tipo humano, que luego
    se ampliará en Los Premios y sobre todo en Rayuela (Los nuestros, Luis Harss)

    En 1961 realiza su primer visita a Cuba, ese mismo año la editorial Fayard publica Los Premios, primera traducción
    de una obra de Cortázar.

    Publica Historias de cronopios y de famas, en la editorial Minotauro, de Buenos Aires y en 1963 aparece Rayuela (Ed.
    Sudamericana), de la que se vendieron 5.000 ejemplares en el primer año. Ese mismo año participa como jurado en el
    Premio Casa de las Américas, en La Habana. .

    Viaja a Chile, en 1970, invitado a la asunción del gobierno del presidente Salvador Allende. La editorial
    Sudamericana publica el libro Relatos, en el que se incluye una selección de cuentos de Bestiario, Final del juego,
    Las armas secretas y Todos los fuegos el fuego.

    En abril del 74 participa en una reunión del Tribunal Russell II reunido en Roma para examinar la situación política
    en América Latina, en particular las violaciones de los derechos humanos. Realiza una visita clandestina a la aldea
    de Solentiname, en Nicaragua.

    En 1981 uno de sus primeros decretos, el gobierno socialista de François Miterrand le otorga la nacionalidad
    francesa, el 24 de julio.

    Entre el 30 de noviembre y el 4 de diciembre de 1983 viaja a Buenos Aires, para visitar a su madre después de la
    caída de la dictadura y la asunción del gobierno por el presidente Raúl Alfonsín. Las autoridades ignoran su
    presencia, pero es calurosamente recibido por la gente, que lo reconoce en las calles.

    El 12 de febrero Julio Cortázar muere y es enterrado en el cementerio de Montparnasse, en la tumba donde yacía Carol
    Dunlop, su última esposa.

    En 1986 la editorial Alfaguara emprende la publicación de las obras completas de Julio Cortázar, incluso aquella que
    habían permanecido inéditas hasta su muerte. Con ese propósito crea una colección especial, Biblioteca Cortázar. El
    diseño de las cubiertas fue confiado a Julio Silva.


